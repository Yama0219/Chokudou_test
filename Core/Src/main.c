/**
  * @brief  Period elapsed callback in non blocking mode
  * @note   This function is called  when TIM15 interrupt took place, inside
  * HAL_TIM_IRQHandler(). It makes a direct call to HAL_IncTick() to increment
  * a global variable "uwTick" used as application time base.
  * @param  htim : TIM handle
  * @retval None
  */
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
  /* USER CODE BEGIN Callback 0 */

  /* USER CODE END Callback 0 */
  if (htim->Instance == TIM15) {
    HAL_IncTick();
  }
  /* USER CODE BEGIN Callback 1 */
  if (htim->Instance == TIM2) {
//    printf("TIM2\r\n");

    if (pos_filtered < TOGGLE_CONT_TH) {
      // speed control
      HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_RESET);
      duty += calc_output_PID_AW(target_speed, curr_speed_filtered, 0.001);
    } else {
      //position control
//      printf("pos control\r\n");
      HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_SET);
      duty = calc_output_PID_AW_pos(ADC_TOP, ADC_VAL, 0.001);
    }


    if (duty > 970) {
      duty = 970;
    } else if (duty < -970) {
      duty = -970;
    }
    if ((duty <= 0)) {
      __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, (1000+(int)duty));
      HAL_GPIO_WritePin(GPIOA, GPIO_PIN_9, GPIO_PIN_RESET);
//      HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_RESET);
    } else {
      __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, (1000-(int)duty));
      HAL_GPIO_WritePin(GPIOA, GPIO_PIN_9, GPIO_PIN_SET);
//      HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_SET);
    }

  }

  if (htim->Instance == TIM4) {
    pos_filtered = pos_filtered * POS_FILTER_C + (1 - POS_FILTER_C) * ADC_VAL_to_pos(ADC_VAL);
    curr_speed_filtered = SPEED_FILTER_C * curr_speed_filtered + (1 - SPEED_FILTER_C) * ((pos_filtered - pre_pos) / 0.001);
    pre_pos = pos_filtered;
//    target = 2000;

    timer += 0.001f;
    if (HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_1) == GPIO_PIN_SET) {
//      printf("%.3f, %d, %.3f\r\n", pos_filtered, ADC_VAL, curr_speed_filtered);
      printf("%.3f\r\n", curr_speed_filtered);
    }
  }

  /* USER CODE END Callback 1 */
}

